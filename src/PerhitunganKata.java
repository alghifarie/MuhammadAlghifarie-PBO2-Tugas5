    private void hitungKataDanKalimat() {
        String teks = jTextArea1.getText();
        
        String[] kataArray = teks.trim().split("\\s+");
        int jumlahKata = teks.isEmpty() ? 0 : kataArray.length;

        String[] kalimatArray = teks.trim().split("[.!?]+");
        int jumlahKalimat = teks.isEmpty() ? 0 : kalimatArray.length;
        
        String[] paragrafArray = teks.trim().split("\\n+");
        int jumlahParagraf = teks.isEmpty() ? 0 : paragrafArray.length;

        outputKata.setText(String.valueOf(jumlahKata));
        outputKalimat.setText(String.valueOf(jumlahKalimat));
        outputParagraf.setText(String.valueOf(jumlahParagraf));
    
    }

